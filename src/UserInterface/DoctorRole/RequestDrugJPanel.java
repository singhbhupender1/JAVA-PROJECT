     private Boolean checkBlankInput()
    {
        if(mOneJTextField.getText().length()==0
            ||mTwoJTextField.getText().length()==0
            ||mThreeJTextField.getText().length()==0
            ||dOneJTextField.getText().length()==0
            ||dTwoJTextField.getText().length()==0
            ||dThreeJTextField.getText().length()==0)
            {
                return false;
            }
        else
        {
            return true;
        }
    }  
    
