    private void requestDrugJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestDrugJButtonActionPerformed
       
        if(checkBlankInput()){
        int selectedRow = patientDrugJTable.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please Select A Row", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        AppointmentVisitRequest request = (AppointmentVisitRequest)patientDrugJTable.getValueAt(selectedRow, 0);
        
        request.setStatus2("Sent");
     
        String medicine1 = mOneJTextField.getText();
        request.setMedicineOne(medicine1);
        
        String medicine2 = mTwoJTextField.getText();
        request.setMedicineTwo(medicine2);
        
        String medicine3 = mThreeJTextField.getText();
        request.setMedicineThree(medicine3);
        
       
        
        int dosage1 = (Integer.parseInt(dOneJTextField.getText()));
        request.setDosageOne(dosage1);
        
        int dosage2 = (Integer.parseInt(dTwoJTextField.getText()));
        request.setDosageTwo(dosage2);
        
        int dosage3 = (Integer.parseInt(dThreeJTextField.getText()));
        request.setDosageThree(dosage3);
        
    
            Organization org = null;
            Enterprise ent = null;
          
      
        for (Network net : business.getNetworkList()) {
            for (Enterprise enter : net.getEnterpriseDirectory().getEnterpriseList()) {
                if (enter instanceof PharmacyEnterprise) {
                    for (Organization organization : enter.getOrganizationDirectory().getOrganizationList()) {
                        if (organization instanceof ChemistOrganization) {
                            org = (ChemistOrganization) organization;
                            break;
            }
           }
          }
         }
        }
        
        if(org != null)
            {
                
            org.getWorkQueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
        
            
            }
          
    JOptionPane.showMessageDialog(null, "Request has been sent to the preferred Chemist.",
                    "Information", JOptionPane.INFORMATION_MESSAGE);
    
    populatepatientJTable();
        }
        
        else{
             JOptionPane.showMessageDialog(null, "Please fill all the values.",
                    "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_requestDrugJButtonActionPerformed

