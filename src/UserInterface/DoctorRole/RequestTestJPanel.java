    public void populatepatientJTable(){
        
        patientTestJTable.setFont(new Font("Comic Sans MS", Font.BOLD, 10));
        patientTestJTable.getTableHeader().setFont(new Font("Comic Sans MS", Font.BOLD, 10));
        
     DefaultTableModel model = (DefaultTableModel)patientTestJTable.getModel();
        
        model.setRowCount(0);
        
        for(WorkRequest request : org.getWorkQueue().getWorkRequestList())
        
        {
            AppointmentVisitRequest req = (AppointmentVisitRequest)request;
            if(req.getReceiver()==userAccount){
                
            Object[] row = new Object[4];
            
            row[0] = req;
            row[1] = req.getSender().getEmployee();
            row[2] = req.getAppointmentDate(); 
            row[3] = req.getStatus1();
            
            
            
            model.addRow(row);
        }}
    }
   
