    private void requestDrugJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestDrugJButtonActionPerformed
    if(checkBlankInput()){
        
       int selectedRow = patientTestJTable.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please Select A Row", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
       
         AppointmentVisitRequest request = (AppointmentVisitRequest)patientTestJTable.getValueAt(selectedRow, 0);

         request.setStatus3("Sent");
      
        String test1 = tOneJTextField.getText();
        request.setTestOne(test1);

        String test2 = tTwoJTextField.getText();
        request.setTestTwo(test2);

        String test3 = tThreeJTextField.getText();
        request.setTestThree(test3);
        
        Organization org = null;
        Enterprise ent = null;

        for(Network net : business.getNetworkList())  {
            for (Enterprise enter : net.getEnterpriseDirectory().getEnterpriseList())    {
                if(enter instanceof PathologyLabEnterprise)  {
                    for (Organization organization : enter.getOrganizationDirectory().getOrganizationList())   {
                        if (organization instanceof LabAssistantOrganization){
                            org = (LabAssistantOrganization)organization;
                            break;
                        }
                    }
                }
            }
        }
        if(org != null)
        {
            org.getWorkQueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
        
        }
        
     JOptionPane.showMessageDialog(null, "Request has been sent to the preferred CLab Assistant.",
                    "Information", JOptionPane.INFORMATION_MESSAGE);
      populatepatientJTable(); 
  
      }
    
    else{

      
      JOptionPane.showMessageDialog(null, "Please fill all fields.",
                    "Warning", JOptionPane.WARNING_MESSAGE);
      }
                   
    }//GEN-LAST:event_requestDrugJButtonActionPerformed

