    private void deleteJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteJButtonActionPerformed
        // TODO add your handling code here:
        int row = userJTable.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else{
        int flag = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete the user account?", "Confirmation", JOptionPane.YES_NO_OPTION);
        if (flag == 0) {
        for(Organization org : enterprise.getOrganizationDirectory().getOrganizationList()){
            
                UserAccount ua = (UserAccount)userJTable.getValueAt(row, 0);
                org.getUserAccountDirectory().removeUserAccount(ua);   
        }
        }
        }
       popData();
    }//GEN-LAST:event_deleteJButtonActionPerformed

