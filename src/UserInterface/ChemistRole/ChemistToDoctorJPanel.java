    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed
        
        int selectedRow = workRequestJTable.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please Select A Row", "Warning", JOptionPane.WARNING_MESSAGE);
            
            return;
        }
        
        AppointmentVisitRequest request = (AppointmentVisitRequest)workRequestJTable.getValueAt(selectedRow, 0);
     
        request.setStatus2("Processing");
        
        ChemistProcessDrugJPanel cpdjp = new  ChemistProcessDrugJPanel(userProcessContainer, request);
        userProcessContainer.add("ChemistProcessDrugJPanel", cpdjp);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
    }//GEN-LAST:event_processJButtonActionPerformed

