    public void populateTable(){
        
        workRequestJTable.setFont(new Font("Comic Sans MS", Font.BOLD, 10));
        workRequestJTable.getTableHeader().setFont(new Font("Comic Sans MS", Font.BOLD, 10));
        
        DefaultTableModel model = (DefaultTableModel)workRequestJTable.getModel();
        
        model.setRowCount(0);
        
           for(WorkRequest request : chemistOrganization.getWorkQueue().getWorkRequestList()){
            
               AppointmentVisitRequest drug = (AppointmentVisitRequest)request;
               if(drug.getpPreferredChemist()==userAccount){
            
            Object[] row = new Object[11];
            
            row[0] = drug;
            row[1] = drug.getpPreferredChemist();
            row[2] = drug.getReceiver();
            row[3] = drug.getSender();
            row[4] =(drug.getMedicineOne())+"-"+(drug.getDosageOne()+"mg");
            row[5] =(drug.getMedicineTwo())+"-"+(drug.getDosageTwo()+"mg");
            row[6] =(drug.getMedicineThree())+"-"+(drug.getDosageThree()+"mg");
            row[7] = drug.getRequestDate();
            row[8] = drug.getResolveDate();
            row[9] = drug.getStatus2();
            row[10] = drug.getPrescriptionResult();
            
            model.addRow(row);
            }
        }
     }

